# corporations_db.py - База данных военных корпораций по странам
# Каждая корпорация производит конкретные типы техники из структуры army в states.json

class Corporation:
    """Класс, представляющий военную корпорацию"""
    def __init__(self, id, name, country, description, specialization, products, founded=None, website=None, logo=None):
        self.id = id
        self.name = name
        self.country = country
        self.description = description
        self.specialization = specialization  # Список типов техники, которые производит
        self.products = products  # Продукты с привязкой к типам из army
        self.founded = founded
        self.website = website
        self.logo = logo
    
    def get_product(self, product_key):
        return self.products.get(product_key)
    
    def get_all_products(self):
        return list(self.products.values())
    
    def has_specialization(self, spec):
        return spec in self.specialization


