# Словарь для быстрого поиска корпорации по ID
CORPORATIONS_BY_ID = {}
for country, corps in ALL_CORPORATIONS.items():
    for corp_id, corp in corps.items():
        CORPORATIONS_BY_ID[corp.id] = corp
        CORPORATIONS_BY_ID[corp_id] = corp

def get_corporations_by_country(country_name):
    """Получить все корпорации страны"""
    return ALL_CORPORATIONS.get(country_name, {})

def get_corporation(corp_id):
    """Получить корпорацию по ID"""
    return CORPORATIONS_BY_ID.get(corp_id)

def get_corporations_by_specialization(specialization):
    """Получить все корпорации с указанной специализацией"""
    result = []
    for corps in ALL_CORPORATIONS.values():
        for corp in corps.values():
            if specialization in corp.specialization:
                result.append(corp)
    return result

def get_all_corporations():
    """Получить все корпорации"""
    result = []
    for corps in ALL_CORPORATIONS.values():
        result.extend(corps.values())
    return result
